public class gato {
private  int edad;
private int peso;
private String nombre;
private String raza; 

 public int getEdad() {
     return edad;
 }
 public void setEdad(int edad) {
     this.edad = edad;
 }
 public String getNombre() {
     return nombre;
 }

 public void setNombre(String nombre) {
     this.nombre = nombre;
 }
 public int getPeso() {
     return peso;
 }
 public void setPeso(int peso) {
     this.peso = peso;
 }
 public String getRaza() {
     return raza;
 }
 public void setRaza(String raza) {
     this.raza = raza;
 }


}
